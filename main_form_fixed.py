@app.get("/health")
def health():
    try:
        validate_db_config()
        validate_graph_config()
        ensure_database()
        ensure_workbook_dir()

        return jsonify({
            "status": "ok",
            "database": "connected-config-present",
            "local_excel": str(EXCEL_FILE_PATH),
            "onedrive_user": ONEDRIVE_USER,
            "onedrive_folder": ONEDRIVE_FOLDER,
        })
    except Exception as exc:
        return jsonify({"status": "error", "message": str(exc)}), 500


@app.get("/next-sno")
def next_sno():
    try:
        return jsonify({"next_sno": get_next_sno()})
    except Exception as exc:
        print("ERROR in /next-sno:", exc)
        traceback.print_exc()
        return jsonify({"next_sno": "", "error": str(exc)}), 500


@app.post("/submit")
def submit():
    try:
        data = request.get_json(force=True) or {}

        error_message = validate_payload(data)
        if error_message:
            return jsonify({"success": False, "message": error_message}), 400

        # Step 1: Save into PostgreSQL
        inserted = save_row_to_db(data)

        # Step 2: Rebuild Excel from DB
        export_db_to_excel()

        # Step 3: Upload Excel to OneDrive
        try:
            uploaded = upload_to_onedrive()
            return jsonify({
                "success": True,
                "message": "Data PostgreSQL me save ho gaya, Excel generate ho gayi, aur OneDrive pe upload bhi ho gaya.",
                "assigned_sno": inserted["sno"],
                "created_at_utc": inserted["created_at_utc"],
                "local_excel": str(EXCEL_FILE_PATH),
                "onedrive_file": uploaded,
            })
        except Exception as upload_exc:
            print("ERROR in OneDrive upload:", upload_exc)
            traceback.print_exc()

            return jsonify({
                "success": False,
                "saved_in_db": True,
                "excel_generated": True,
                "message": "Data PostgreSQL me save ho gaya aur Excel generate ho gayi, lekin OneDrive upload fail ho gaya.",
                "assigned_sno": inserted["sno"],
                "created_at_utc": inserted["created_at_utc"],
                "local_excel": str(EXCEL_FILE_PATH),
                "upload_error": str(upload_exc),
            }), 502

    except Exception as exc:
        print("ERROR in /submit:", exc)
        traceback.print_exc()
        return jsonify({"success": False, "message": str(exc)}), 500


